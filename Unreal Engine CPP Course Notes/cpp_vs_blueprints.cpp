/*
	C++ VS. Blueprint Notes

	SOURCE: https://www.udemy.com/course/unrealcourse/learn/lecture/51564731#overview

	----------------------------------------------------------------------------

	- C++ is the general purpose programming language
	- Blueprint is the Visual Scripting System that is built into the Unreal Engine

	-They compliment each other and meant to be used together

	Blueprint Strengths
	-------------------------
	- Easier to learn
	- Programmed inside of Unreal Engine
	- Quick to make changes and good for prototyping
		- Gets messy as you build bigger games though
	- Writing gameplay code


	C++ Strengths
	-------------------------
	- Gives full control of everything in Unreal Engine (and beyond)
		- Can use other people's libraries, not just Unreal
	- Much fast than blueprint
	- More concise
	- Better for writing bigger game and non-gameplay code




	- Blueprints can access the C++ code
		- C++ can't access the blueprints
*/